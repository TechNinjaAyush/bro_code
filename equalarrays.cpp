class Solution{
    public:

    //Function to check if two arrays are equal or not.
    bool check(vector<ll> A, vector<ll> B, int N) {
     sort ( A.begin() ,  A.end()) ;
     sort ( B.begin() , B.end()) ;
      vector< pair <long long,long long > > vect;
      for ( int i = 0 ; i< N ;i++){
          vect.push_back(make_pair(A[i] , B[i])) ;
          
      }
      
      for (auto it : vect){
          if(it.first != it.second){
              return false ;
          }
      }
      return true ;
    }
};

